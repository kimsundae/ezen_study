package 과제.과제10;

public class Member {
	String id;
	String password;
	String name;
	String phoneNumber;
	int age;
	
	public Member(String id, String password, String name, String phoneNumber, int age) {
		super();
		this.id = id;
		this.password = password;
		this.name = name;
		this.phoneNumber = phoneNumber;
		this.age = age;
	}

}
